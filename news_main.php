            <p>The last male winner of cycling's triple crown, Stephen Roche, has 'welcomed <a href="#">Tadej Pogačar</a> to the club' after he emulated the Irishman and Eddy Merckx and became the third man in history to win the <a href="#">Giro d’Italia, Tour de France</a> and <a href="#">World Championships</a> in the same season.</p>
            <p>Pogačar ended the 37-year wait for the feat to be repeated in men's racing, with Annemiek van Vleuten netting the only women's triple crown in 2022, <a href="#">doing so with an outrageous 100km to-go attack</a> and subsequent 50km solo ride in Zürich on Sunday.</p>
            <p>Roche was present in Switzerland to congratulate Pogačar, the first-ever Slovenian world champion, sharing an embrace post-race and posing for pictures in the mixed zone while holding up three fingers to symbolise the triple.</p>
            <p>"I feel like I'm in a sandwich now between Merckx and Pogačar which is not bad. But I think it's really great because it makes the whole thing a little more human," said Roche speaking to Dutch broadcaster NOS. </p>
            <p>"It's 37 years since I won it, I was 13 years after Merckx, but everybody was saying it's not possible - 'A new era', 'Different generations', 'It can't be done again'.</p>
            <p>"I believe records are there to be broken and beaten and improved upon, so for me, it's great that it's this particular person that has done the triple and I'm delighted to be here to give him the keys to the house."</p>
            <p>In similar fashion to many of Pogačar's competitors on the day, Roche was stunned by the 26-year-old's ability to stay away once he'd made his audacious move out of the peloton and committed to a long-range move akin to that of Merckx's generation of racing, choosing to simply sum up the win as "phenomenal".</p>
